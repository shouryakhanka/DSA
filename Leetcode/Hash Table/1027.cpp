int solve(int index, vector<int> nums, int diff)
{
    // backward check

    int ans = 0;

    if (index < 0)
        return 0;

    for (int i = index - 1; i >= 0; i--)
    {
        if (nums[index] - nums[i] == diff)
        {
            ans = max(ans, 1 + solve(i, nums, diff));
        }
    }
    return ans;
}

int longestArithSeqLength(vector<int> &nums)
{
    int n = nums.size();
    if (n <= 2)
        return n;

    int ans = 0;

    for (int i = 0; i < n; i++)
    {
        for (int j = i + 1; j < n; j++)
        {
            int back = solve(i, nums, nums[j] - nums[i]);
            ans = max(ans, 2 + back);
        }
    }
    return ans;
}

